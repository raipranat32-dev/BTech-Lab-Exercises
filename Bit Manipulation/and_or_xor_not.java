public class and_or_xor_not{
    public static void main(String args[]){
        int a=5,b=3;
        System.out.println("5 & 3 = "+(a&b));
        System.out.println("5 | 3 = "+(a|b));
        System.out.println("5 ^ 3 = "+(a^b));
        System.out.println("~ a = "+(~a));
        System.out.println("~ 0 ="+(~0));
    }
}