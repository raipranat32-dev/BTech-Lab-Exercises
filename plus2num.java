/**
 * Adds two integers and return their sum
 * @param a First Number
 * @param b Second Number
 * @return The sum of a and b
 *
 */
public class plus2num 
{
    public static void main(String [] args)
    {
        int a=12;
        int b=10;
        int sum = a+b;
        System.out.println("Sum of two numbers "+a+" and "+b+" is equal to "+sum);
    }
}
// OUTPUT
// Sum of two numbers 12 and 10 is equal to 22 
